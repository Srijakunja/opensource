import java.io.*;
import java.util.*;

public class Solution {

    public static void main(String[] args) {
        Scanner sc = new Scanner(System.in);
        String key1=sc.nextLine();
        String key2=sc.nextLine();
        if(key1.equals(key2)){
            System.out.println("YES");
        }else{
            System.out.println("NO");
        }
    }
}
